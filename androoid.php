				<p>کاربران گرامی برای اتصال به سرویس وی‌پی‌ان درایفون و آیپد به صورت زیر اقدام نمایید .<br />
مراحل زیر را دنبال کنید :<br />
Settings –> General –> Network –> VPN –> Add VPN Configuration –> PPTP<br />
سرور را وارد کنید:<br />
Account: یوزرنیم سرویس را وارد کنید .<br />
RSA securID: این قسمت را روی گزینه Off قرار دهید .<br />
Password: پسورد خود را وارد کنید .<br />
Encryption level: را بر روی None قرار دهید .<br />
Send all traffic: را بر روی On قرار دهید .<br />
Proxy: را بر روی Off قرار دهید .<br />
سپس تنظیمات را ذخیره کنید و با انتخاب کانکشن و وارد کردن یوزرنیم و پسورد خریداری شده ، متصل شوید.<br />
